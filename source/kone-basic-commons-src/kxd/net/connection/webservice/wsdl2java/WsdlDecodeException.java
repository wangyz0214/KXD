package kxd.net.connection.webservice.wsdl2java;

public class WsdlDecodeException extends Exception {
	private static final long serialVersionUID = 1L;

	public WsdlDecodeException() {
		super();
	}

	public WsdlDecodeException(String message, Throwable cause) {
		super(message, cause);
	}

	public WsdlDecodeException(String message) {
		super(message);
	}

	public WsdlDecodeException(Throwable cause) {
		super(cause);
	}

}
